int		ft_sqrt(int i)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt <= 46340 && sqrt <= i)
	{
		if (sqrt * sqrt == i)
			return (sqrt);
		sqrt++;
	}
	return (-1);
}
